x = int(input("enter the value : "))

if(x == 0):
    print("sunday")
elif(x == 1):
    print("monday")
elif(x == 2):
    print("tuesday")
elif(x == 3):
    print("wednesday")
elif(x == 4):
    print("thursday")
elif(x == 5 ):
    print("friday")
elif(x == 6):
    print("saturday")
else:
    print("enter valid number")
